    HTMLTableRowElement,
    React.HTMLAttributes<HTMLTableRowElement>
>(({ className, ...props }, ref) => (
    <tr
        ref={ref}
        className={cn(
            "border-b transition-colors hover:bg-[var(--sl-container-calm)] data-[state=selected]:bg-[var(--sl-container-calm)]",
            className
        )}
        {...props}
    />
));
TableRow.displayName = "TableRow";

