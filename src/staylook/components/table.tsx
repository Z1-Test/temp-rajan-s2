/**
 * Staylook Table Component
 * 
 * Design Principles:
 * - Editorial: Clear separation of rows and columns
 * - Minimal: Pure functional layout
 * - Curved: Follows container/card radius where applicable
 */

import * as React from 'react';
import { cn } from '@/lib/utils';

const Table = React.forwardRef<
    HTMLTableElement,
    React.HTMLAttributes<HTMLTableElement>
>(({ className, ...props }, ref) => (
    <div className="relative w-full overflow-auto rounded-[var(--sl-radius-card)] border border-[var(--sl-outline-muted)] bg-[var(--sl-container-muted)]">
        <table
            ref={ref}
            className={cn("w-full caption-bottom text-[length:var(--sl-text-sm)]", className)}
            {...props}
        />
    </div>
));
Table.displayName = "Table";

